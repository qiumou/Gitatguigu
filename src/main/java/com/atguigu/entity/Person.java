package com.atguigu.entity;

public class Person {
	
	private Integer Pno;
	private String Pname;
	
	public Person() {}

	public Integer getPno() {
		return Pno;
	}

	public String getPname() {
		return Pname;
	}

	public void setPno(Integer pno) {
		Pno = pno;
	}

	public void setPname(String pname) {
		Pname = pname;
	}

}
